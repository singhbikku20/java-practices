package org.controlStructandBasicOps;

public class CastingConnoisseur {

    public static void main(String[] args){

        double a = 5.7;
        int b   = 2;
        int c   = (int) a;
        double d = b;

        System.out.println("Double a: " + a);
        System.out.println("Int b: " + b);
        System.out.println("Converted a: " + c);
        System.out.println("Converted b: " + d);

    }

}
