package org.example;

public class ConditionalConundrum{

    public static void main (String[] args){

        int number = 5;

        if(number == 0){
            System.out.println("The Number is zero");
        }
        else if (number >0){
            System.out.println("The Number is positive");
        } else {
            System.out.println("The Number is negative");
        }

    }
}
